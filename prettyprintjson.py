import os
import json
import gspread
from google.colab import auth, drive
from google.auth import default
from datetime import datetime

# --- Helper Functions ---

def find_transcript_pairs(folder_path, processed_files):
    """
    Scans for transcript pairs (dataBEFORE.txt and dataAFTER.txt)
    and filters out already processed ones.
    """
    files = os.listdir(folder_path)
    before_files = [f for f in files if f.endswith('|dataBEFORE.txt')]
    new_pairs = []
    for before in before_files:
        try:
            base_name, _ = before.rsplit('|', 1)
        except ValueError:
            continue
        after = f"{base_name}|dataAFTER.txt"
        if after in files and after not in processed_files:
            new_pairs.append((
                os.path.join(folder_path, before),
                os.path.join(folder_path, after),
                after,
                base_name
            ))
    return new_pairs

def pretty_print_json(input_path, output_path):
    """
    Reads a file containing a JSON string, pretty prints it with sorted keys,
    and saves it to a new .json file.
    """
    try:
        with open(input_path, 'r') as f:
            json_string = f.read()
            # Attempt to load the JSON data
            data = json.loads(json_string)
            # Write the pretty-printed JSON to the output file
            with open(output_path, 'w') as out_f:
                json.dump(data, out_f, indent=4, sort_keys=True)
        return True
    except json.JSONDecodeError:
        print(f"  - Error: Could not decode JSON from {input_path}")
        return False
    except Exception as e:
        print(f"  - An unexpected error occurred: {e}")
        return False


