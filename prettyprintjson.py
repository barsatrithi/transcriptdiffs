# --- Configuration ---
# The folder in your Google Drive where the .txt files are located.
CALL_LOGS_FOLDER = "/content/drive/MyDrive/test"
# The name of the Google Sheet to update.
SHEET_NAME = "JSON Pretty Print Log"


# --- Google Sheet Setup ---
try:
    sh = gc.open(SHEET_NAME)
    worksheet = sh.sheet1
    print(f"Successfully opened Google Sheet: '{SHEET_NAME}'")
except gspread.exceptions.SpreadsheetNotFound:
    print(f"Spreadsheet '{SHEET_NAME}' not found. Creating a new one.")
    sh = gc.create(SHEET_NAME)
    worksheet = sh.sheet1
    # Set up the header row with timestamp column
    worksheet.append_row([
        "AI Phone Number",
        "Client Phone Number",
        "File Reference",
        "Process Timestamp"  # New column for timestamp
    ])
    # Share the sheet with your email to ensure you have access
    sh.share(creds.service_account_email, perm_type='user', role='writer')


# --- Processing Logic ---
# Get a list of already processed files from the Google Sheet
processed_files = set(worksheet.col_values(3)[1:])  # Column 3 is File Reference
# Find new pairs of transcripts to process
transcript_pairs = find_transcript_pairs(CALL_LOGS_FOLDER, processed_files)

if not transcript_pairs:
    print("\nNo new files have been processed")
else:
    print(f"\nFound {len(transcript_pairs)} new file pair(s) to process...")

    for before_path, after_path, after_filename, base_name in transcript_pairs:
        print(f"\nProcessing pair: {base_name}")

        # --- Pretty Print JSON ---
        before_json_path = os.path.join(CALL_LOGS_FOLDER, f"{base_name}|dataBEFORE.json")
        after_json_path = os.path.join(CALL_LOGS_FOLDER, f"{base_name}|dataAFTER.json")

        # Process the BEFORE file
        if pretty_print_json(before_path, before_json_path):
            print(f"  - Successfully created: {before_json_path}")

        # Process the AFTER file
        if pretty_print_json(after_path, after_json_path):
            print(f"  - Successfully created: {after_json_path}")

        # --- Update Google Sheet ---
        try:
            parts = after_filename.split('|')
            ai_number = parts[0]
            client_number = parts[1]

            # Get current timestamp
            process_timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")

            worksheet.append_row([
                ai_number,
                client_number,
                after_filename,  # Using the .txt filename as the reference
                process_timestamp  # Add the timestamp
            ])
            print(f"  - Updated Google Sheet for: {after_filename} at {process_timestamp}")
        except IndexError:
            print(f"  - Warning: Could not parse AI and Client numbers from filename: {after_filename}")
            continue

    print("\n✅ All new JSON files have been pretty-printed and the log has been updated.")

