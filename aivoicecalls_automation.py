def get_all_keys(data, parent_key='', sep='.'):
    """Recursively gets all keys from a nested dictionary."""
    items = []
    for key, value in data.items():
        new_key = parent_key + sep + key if parent_key else key
        if isinstance(value, dict) and value:
            items.extend(get_all_keys(value, new_key, sep=sep))
        else:
            items.append(new_key)
    return set(items)

def get_all_string_values(data):
    """Recursively gets all string values and concatenates them."""
    all_text = []
    if isinstance(data, dict):
        for value in data.values():
            all_text.append(get_all_string_values(value))
    elif isinstance(data, list):
        for item in data:
            all_text.append(get_all_string_values(item))
    else:
        all_text.append(str(data) if data is not None else "")
    return " ".join(all_text)

def get_value_by_path(data, path):
    """Navigates a dictionary using a dot-separated path."""
    for key in path.split('.'):
        data = data[key]
    return data

def calculate_per_key_metrics(before_data, after_data):
    """
    Calculates WER and CER for only the values that have been modified
    between the two JSON objects.
    """
    before_keys = get_all_keys(before_data)
    after_keys = get_all_keys(after_data)
    common_keys = before_keys.intersection(after_keys)

    per_key_results = {}

    for key_path in common_keys:
        try:
            before_value = get_value_by_path(before_data, key_path)
            after_value = get_value_by_path(after_data, key_path)

            if before_value != after_value:
                before_str = str(before_value)
                after_str = str(after_value)

                per_key_results[key_path] = {
                    "before": before_value,
                    "after": after_value,
                    "WER": round(wer(after_str, before_str), 4),
                    "CER": round(cer(after_str, before_str), 4)
                }
        except KeyError:
            continue

    return per_key_results

