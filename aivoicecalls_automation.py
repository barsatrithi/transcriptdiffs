def create_structured_diff(before_data, after_data):
    """
    Performs a deep, recursive comparison of two dictionaries to generate a
    structured diff, identifying the exact path to each change.
    (Fuzzy logic has been removed as requested).
    """
    diff = {
        "added_keys": {},
        "removed_keys": {},
        "modified_values": {}
    }

    # This recursive helper function will do the deep comparison
    def _compare_items(before_item, after_item, path):
        # If items are dictionaries, recurse deeper
        if isinstance(before_item, dict) and isinstance(after_item, dict):
            before_keys = set(before_item.keys())
            after_keys = set(after_item.keys())
            common_keys = before_keys.intersection(after_keys)

            # 1. Find keys added and removed at this level
            added_keys = after_keys - before_keys
            for key in added_keys:
                current_path = ".".join(path + [key])
                diff["added_keys"][current_path] = after_item[key]

            removed_keys = before_keys - after_keys
            for key in removed_keys:
                current_path = ".".join(path + [key])
                diff["removed_keys"][current_path] = before_item[key]

            # 2. Recurse on common keys
            for key in common_keys:
                _compare_items(before_item[key], after_item[key], path + [key])

        # If items are lists, compare them element by element
        elif isinstance(before_item, list) and isinstance(after_item, list):
            if before_item != after_item:
                current_path = ".".join(path)
                diff["modified_values"][current_path] = {
                    "before": before_item,
                    "after": after_item
                }
        # Base case: items are not dicts/lists, compare them directly
        elif before_item != after_item:
            current_path = ".".join(path)
            diff["modified_values"][current_path] = {
                "before": before_item,
                "after": after_item
            }

    _compare_items(before_data, after_data, [])
    return diff


