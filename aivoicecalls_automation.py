def normalize_text(text: str) -> str:
    """Normalize text for comparison by removing extra spaces and converting to lowercase."""
    if not isinstance(text, str):
        return ""
    return " ".join(text.lower().split())

def create_html_diff(before_text, after_text, output_path):
    """Generates a side-by-side HTML diff file from two strings."""
    before_lines = before_text.splitlines()
    after_lines = after_text.splitlines()
    html_diff = difflib.HtmlDiff(wrapcolumn=80).make_file(
        before_lines,
        after_lines,
        fromdesc='Before (AI Transcript)',
        todesc='After (Revised Transcript)'
    )
    with open(output_path, "w", encoding="utf-8") as f:
        f.write(html_diff)
    return True

def generate_inline_diff(before_text, after_text):
    """Generates inline text diffs using +/- markup."""
    before_words = before_text.split()
    after_words = after_text.split()
    diff = ndiff(before_words, after_words)

    result_words = []
    for token in diff:
        if token.startswith("- "):
            result_words.append(f"{{-{token[2:]}-}}")  # deletion
        elif token.startswith("+ "):
            result_words.append(f"{{+{token[2:]}+}}")  # insertion
        else:
            result_words.append(token[2:])
    return " ".join(result_words)

